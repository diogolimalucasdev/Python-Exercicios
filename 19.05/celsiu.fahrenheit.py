print("converter de celsius para fahrenheit")
celsius = float(input("Insiria o valor da temperatura em graus Celsius"))

fahrenheit = (9 * (celsius / 5)) + 32

kelvin = celsius + 273.15

print("O valor de Celsius: ", celsius, "Em fahrenheit é : ", fahrenheit, "ºF")

print("O valor de Celsius: ", celsius, "Em kelvin é: ", kelvin,"ºK")



fahrenheit_novo = float(input("Digite um valor de Fahrenheit: "))

